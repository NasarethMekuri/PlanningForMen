    private void applySearchResults()
    {
        
        _dieselList.clear();
        _normalList.clear();
        _tuningList.clear();
        _finishedList.clear();
        
        for (Service s : _garages[0].getJobs())
        {
            _dieselList.addElement(s);
        }
        for (Service s : _garages[1].getJobs())
        {
            _normalList.addElement(s);
        }
        for (Service s : _garages[2].getJobs())
        {
            _tuningList.addElement(s);
        }
        for (Service s : _controller.getFinishedJobs())
        {
            _finishedList.addElement(s);
        }
    }
